-- Task 7: Which city generated the highest revenue?

SELECT City,
       SUM(Quantity * Price) AS Revenue
FROM Customers c
INNER JOIN Orders o
ON c.CustomerID = o.CustomerID
INNER JOIN Products p
ON o.ProductID = p.ProductID
GROUP BY City
ORDER BY Revenue DESC
LIMIT 1;


-- Task 8: Show the average order value for each customer

SELECT CustomerName,
       AVG(Quantity * Price) AS AverageOrderValue
FROM Customers c
INNER JOIN Orders o
ON c.CustomerID = o.CustomerID
INNER JOIN Products p
ON o.ProductID = p.ProductID
GROUP BY CustomerName;


-- Task 9: Display the top 3 highest-spending customers

SELECT CustomerName,
       SUM(Quantity * Price) AS TotalSpent
FROM Customers c
INNER JOIN Orders o
ON c.CustomerID = o.CustomerID
INNER JOIN Products p
ON o.ProductID = p.ProductID
GROUP BY CustomerName
ORDER BY TotalSpent DESC
LIMIT 3;


-- Task 10: Display customers who spent more than the average customer spending

SELECT CustomerName,
       SUM(Quantity * Price) AS TotalSpent
FROM Customers c
INNER JOIN Orders o
ON c.CustomerID = o.CustomerID
INNER JOIN Products p
ON p.ProductID = o.ProductID
GROUP BY CustomerName
HAVING SUM(Quantity * Price) >
(
    SELECT AVG(TotalSpent)
    FROM
    (
        SELECT SUM(Quantity * Price) AS TotalSpent
        FROM Orders o
        INNER JOIN Products p
        ON o.ProductID = p.ProductID
        GROUP BY CustomerID
    ) AS CustomerTotals
)
ORDER BY TotalSpent DESC;

