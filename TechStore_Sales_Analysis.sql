-- Task 13: Count the number of orders placed by each customer

SELECT CustomerName,
       COUNT(OrderID) AS NumberOfOrders
FROM Customers c
INNER JOIN Orders o
ON c.CustomerID = o.CustomerID
GROUP BY CustomerName
ORDER BY NumberOfOrders DESC;


-- Task 14: Create a view showing customer spending

CREATE VIEW CustomerSpending AS

SELECT CustomerName,
       SUM(Quantity * Price) AS TotalSpent
FROM Customers c
INNER JOIN Orders o
ON c.CustomerID = o.CustomerID
INNER JOIN Products p
ON o.ProductID = p.ProductID
GROUP BY CustomerName;


-- Task 15: Display customers who spent more than R10,000 using the view

SELECT *
FROM CustomerSpending
WHERE TotalSpent > 10000;


