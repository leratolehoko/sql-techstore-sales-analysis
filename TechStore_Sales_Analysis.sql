-- Task 2: Display products costing more than R1000

SELECT ProductName, Category, Price
FROM Products
WHERE Price > 1000;


-- Task 3: Count the total number of customers

SELECT COUNT(CustomerID) AS TotalCustomers
FROM Customers;


-- Task 4: Calculate revenue generated by each product

SELECT ProductName,
       SUM(Quantity * Price) AS Revenue
FROM Products p
INNER JOIN Orders o
ON p.ProductID = o.ProductID
GROUP BY ProductName
ORDER BY Revenue DESC;


-- Task 5: Display customers who spent more than R10,000

SELECT CustomerName,
       SUM(Quantity * Price) AS TotalSpent
FROM Customers c
INNER JOIN Orders o
ON c.CustomerID = o.CustomerID
INNER JOIN Products p
ON p.ProductID = o.ProductID
GROUP BY CustomerName
HAVING SUM(Quantity * Price) > 10000
ORDER BY TotalSpent DESC;

